/// ВАЛИДАЦИОННЫЕ ТЕСТЫ ФОРМУЛЫ АТЛАСОВА
/// На основе открытых полевых данных из Якутии
///
/// Источники данных:
/// [1] Neleger Station, ИМЗ СО РАН — GTN-P database (gtnpdatabase.org/activelayers/view/98)
///     ALT: лес ~1.0–1.3 м, гарь ~1.6–1.9 м (2001–2020)
/// [2] Churapcha monitoring — Iijima et al. (2018), Remote Sens. 10, 1579
///     ALT: лес 1.3 м, луг 2.0 м (сентябрь 2015)
/// [3] Brouchkov et al. (2004) — ALT 1–4 м (диапазон Центральная Якутия)
/// [4] Fedorov et al. (2022) — постпожарное увеличение ALT на 40–60%
/// [5] Bockheim et al. (2004) — просадка 5–10+ см/год на едоме
/// ============================================================

// ── Физические константы ──────────────────────────────────
const L: f64 = 334_000.0; // Дж/кг — скрытая теплота плавления льда
const RHO_W: f64 = 1000.0; // кг/м³ — плотность воды

// ── Региональные константы формулы Атласова ───────────────
const ALPHA: f64 = 0.60; // коэффициент едомы
const BETA: f64 = 0.45; // коэффициент покрова/пожара
const GAMMA: f64 = 0.12; // коэффициент континентальности
const I0: f64 = 0.30; // пороговая льдистость
const DDT0: f64 = 40.0; // базовая амплитуда, °C

// ── Базовая формула Стефана ───────────────────────────────
fn stefan_depth(lambda_t: f64, ddt: f64, w: f64) -> f64 {
    // ξ₀ = √(2λₜ·DDT / L·ρw·w)
    // DDT конвертируем из °C·сут → °C·с (×86400)
    let ddt_si = ddt * 86_400.0;
    (2.0 * lambda_t * ddt_si / (L * RHO_W * w)).sqrt()
}

// ── Коэффициент покрова/пожара ────────────────────────────
fn k_fire(v: f64) -> f64 {
    // K_fire = exp(β·(1–V))
    (BETA * (1.0 - v)).exp()
}

// ── Функция континентальности ─────────────────────────────
fn f_continental(delta_t: f64) -> f64 {
    // f(ΔT) = 1 + γ·ln(ΔT/ΔT₀)
    1.0 + GAMMA * (delta_t / DDT0).ln()
}

// ── Глубина протаивания по формуле Атласова ───────────────
fn atlasov_thaw_depth(lambda_t: f64, ddt: f64, w: f64, v: f64, delta_t: f64) -> f64 {
    stefan_depth(lambda_t, ddt, w) * k_fire(v) * f_continental(delta_t)
}

// ── Просадка поверхности ──────────────────────────────────
fn atlasov_subsidence(xi: f64, i_ice: f64, k_comp: f64) -> f64 {
    // s = ξ · K_ed · K_comp
    let k_ed = if i_ice > I0 {
        1.0 + ALPHA * (i_ice - I0) / (1.0 - I0)
    } else {
        1.0
    };
    xi * k_ed * k_comp
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 1 — Базовый Стефан (без поправок Якутии)         ║
// ║  Проверяем что чистая формула Стефана работает верно    ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_stefan_baseline_central_yakutia() {
    // Параметры: типичный суглинок, Центральная Якутия, лес
    // λₜ = 1.2 Вт/(м·К) — суглинок в талом состоянии
    // DDT = 1000 °C·сут — Якутск, среднее за 1990–2010
    // w = 0.35 — стандартная льдистость суглинка
    let xi0 = stefan_depth(1.2, 1000.0, 0.35);

    // Ожидаемый диапазон: 1.0–1.6 м
    // Источник: Brouchkov et al. (2004) — ALT 1–4 м для ЦЯ
    //           Iijima et al. (2018) — ALT лес 1.3 м, луг 2.0 м
    println!("Stefan baseline depth: {:.3} m", xi0);
    assert!(
        xi0 >= 0.8 && xi0 <= 2.0,
        "Stefan depth {:.3} m вне диапазона 0.8–2.0 м для ЦЯ",
        xi0
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 2 — Neleger Station: лес (контрольный участок)   ║
// ║  Данные: GTN-P Neleger, ИМЗ СО РАН, 2001–2020          ║
// ║  ALT в ненарушенном лиственничном лесу: 1.0–1.3 м      ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_neleger_intact_forest() {
    // Neleger Station: 62.2°N, 129.4°E, Центральная Якутия
    // λₜ = 1.0 Вт/(м·К) — торфяно-суглинистый грунт под лесом
    // DDT = 900 °C·сут — нижняя граница для Neleger
    // w = 0.30 — умеренная льдистость
    // V = 0.85 — густой лиственничный лес
    // ΔT = 88 °C — характерная амплитуда Центральной Якутии
    let xi = atlasov_thaw_depth(1.0, 900.0, 0.30, 0.85, 88.0);

    println!("Neleger forest ALT (Atlasov): {:.3} m", xi);
    println!("  K_fire(V=0.85) = {:.4}", k_fire(0.85));
    println!("  f(ΔT=88°C)     = {:.4}", f_continental(88.0));

    // Наблюдаемый диапазон по GTN-P: 1.0–1.3 м
    assert!(
        xi >= 0.8 && xi <= 1.5,
        "Neleger forest ALT {:.3} м, ожидается 1.0–1.3 м (GTN-P Neleger)",
        xi
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 3 — Neleger Station: гарь после пожара 2001 г.   ║
// ║  Данные: Fedorov et al. (2022), Land 11(4):496          ║
// ║  ALT на гари: 1.6–1.9 м (+40–60% от контроля)          ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_neleger_post_fire() {
    // После пожара 2001: V ≈ 0.0 (полное уничтожение покрова)
    // Первые годы после пожара
    let xi_control = atlasov_thaw_depth(1.0, 900.0, 0.30, 0.85, 88.0);
    let xi_burned = atlasov_thaw_depth(1.2, 900.0, 0.30, 0.0, 88.0);
    // λₜ увеличена до 1.2 — обнажённый грунт теплопроводнее

    let increase_pct = (xi_burned / xi_control - 1.0) * 100.0;

    println!("Neleger post-fire test:");
    println!("  Контроль (лес):  {:.3} м", xi_control);
    println!("  Гарь (V=0):      {:.3} м", xi_burned);
    println!("  Увеличение ALT:  {:.1}%", increase_pct);
    println!("  K_fire(V=0) =    {:.4}", k_fire(0.0));

    // Fedorov et al. (2022): +40–60% в первые 6–7 лет
    assert!(
        increase_pct >= 30.0 && increase_pct <= 80.0,
        "Постпожарное увеличение {:.1}%, ожидается 40–60% (Fedorov 2022)",
        increase_pct
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 4 — Churapcha: луг vs лес                        ║
// ║  Данные: Iijima et al. (2018) Remote Sens. 10, 1579    ║
// ║  Лес: 1.3 м, луг: 2.0 м (сентябрь 2015)               ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_churapcha_forest_vs_meadow() {
    // Churapcha: 61.6°N, 132.8°E, правый берег Лены
    // DDT ≈ 1050 °C·сут (климатическая норма Churapcha)
    // ΔT = 90°C (среднее для района)

    // Лес: V=0.80, λₜ=1.0, w=0.32
    let xi_forest = atlasov_thaw_depth(1.0, 1050.0, 0.32, 0.80, 90.0);

    // Луг/открытый участок: V=0.30, λₜ=1.4, w=0.28
    let xi_meadow = atlasov_thaw_depth(1.4, 1050.0, 0.28, 0.30, 90.0);

    println!("Churapcha 2015:");
    println!("  Лес (Atlasov):  {:.3} м  (наблюдение: 1.3 м)", xi_forest);
    println!("  Луг (Atlasov):  {:.3} м  (наблюдение: 2.0 м)", xi_meadow);
    println!(
        "  Отношение луг/лес: {:.2}  (наблюдение: 1.54)",
        xi_meadow / xi_forest
    );

    // Iijima (2018): лес 1.3 м → допуск ±0.4 м
    assert!(
        xi_forest >= 0.9 && xi_forest <= 1.7,
        "Churapcha лес {:.3} м, ожидается ~1.3 м",
        xi_forest
    );

    // Луг 2.0 м → допуск ±0.5 м
    assert!(
        xi_meadow >= 1.5 && xi_meadow <= 2.5,
        "Churapcha луг {:.3} м, ожидается ~2.0 м",
        xi_meadow
    );

    // Луг должен быть глубже леса
    assert!(xi_meadow > xi_forest, "Луг должен протаивать глубже леса");
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 5 — Просадка на едоме: Bykovsky Peninsula        ║
// ║  Данные: Günther et al. (2016), ICOP                   ║
// ║  Просадка: 5–10+ см/год на едоме (I_ice=0.55–0.70)     ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_yedoma_subsidence_bykovsky() {
    // Едомный участок, ALT ≈ 0.5 м (активный слой над льдом)
    // I_ice = 0.65 — типичная центральная едома
    // K_comp = 0.10 — сжимаемость едомного грунта
    let xi = 0.5; // м — глубина протаивания
    let subsidence = atlasov_subsidence(xi, 0.65, 0.10);

    // Эквивалент в см/год: при 0.5 м/год протаивания
    // Günther (2016): наблюдаемая просадка 5–10+ см/год
    let subsidence_cm = subsidence * 100.0;

    println!("Yedoma subsidence test:");
    println!("  ALT:       {:.2} м", xi);
    println!("  I_ice:     0.65");
    println!("  K_ed:      {:.4}", 1.0 + ALPHA * (0.65 - I0) / (1.0 - I0));
    println!("  Просадка:  {:.2} см (ожидается 5–10+ см)", subsidence_cm);

    assert!(
        subsidence_cm >= 3.0 && subsidence_cm <= 15.0,
        "Просадка {:.2} см, ожидается 5–10 см (Günther 2016)",
        subsidence_cm
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 6 — Физическая монотонность: DDT                 ║
// ║  Чем больше тепла → тем глубже протаивание             ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_monotonicity_ddt() {
    let base = (1.2, 0.30, 0.75, 88.0);
    let xi_800 = atlasov_thaw_depth(base.0, 800.0, base.1, base.2, base.3);
    let xi_1000 = atlasov_thaw_depth(base.0, 1000.0, base.1, base.2, base.3);
    let xi_1200 = atlasov_thaw_depth(base.0, 1200.0, base.1, base.2, base.3);
    let xi_1400 = atlasov_thaw_depth(base.0, 1400.0, base.1, base.2, base.3);

    println!(
        "DDT monotonicity: {:.3} < {:.3} < {:.3} < {:.3}",
        xi_800, xi_1000, xi_1200, xi_1400
    );

    assert!(
        xi_800 < xi_1000 && xi_1000 < xi_1200 && xi_1200 < xi_1400,
        "ALT должна монотонно расти с DDT"
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 7 — Физическая монотонность: растительность      ║
// ║  Меньше покрова → глубже протаивание                   ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_monotonicity_vegetation() {
    let base = (1.2, 1000.0, 0.30, 88.0);
    let xi_v00 = atlasov_thaw_depth(base.0, base.1, base.2, 0.0, base.3); // гарь
    let xi_v30 = atlasov_thaw_depth(base.0, base.1, base.2, 0.3, base.3); // луг
    let xi_v70 = atlasov_thaw_depth(base.0, base.1, base.2, 0.7, base.3); // редкий лес
    let xi_v90 = atlasov_thaw_depth(base.0, base.1, base.2, 0.9, base.3); // тайга

    println!(
        "Vegetation monotonicity (гарь→тайга): {:.3} > {:.3} > {:.3} > {:.3}",
        xi_v00, xi_v30, xi_v70, xi_v90
    );

    assert!(
        xi_v00 > xi_v30 && xi_v30 > xi_v70 && xi_v70 > xi_v90,
        "ALT должна монотонно убывать с V (больше покров → меньше протаивание)"
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 8 — Физическая монотонность: льдистость           ║
// ║  Больше льда → больше просадка                          ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_monotonicity_ice_content() {
    let xi = 1.5;
    let k_comp = 0.10;
    let s_low = atlasov_subsidence(xi, 0.20, k_comp); // обычный грунт
    let s_medium = atlasov_subsidence(xi, 0.40, k_comp); // умеренная льдистость
    let s_yedoma = atlasov_subsidence(xi, 0.65, k_comp); // едома
    let s_max = atlasov_subsidence(xi, 0.80, k_comp); // максимальная едома

    println!(
        "Ice content monotonicity: {:.3} < {:.3} < {:.3} < {:.3}",
        s_low, s_medium, s_yedoma, s_max
    );

    assert!(
        s_low < s_medium && s_medium < s_yedoma && s_yedoma < s_max,
        "Просадка должна монотонно расти с льдистостью"
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 9 — Контрольный диапазон К_fire                  ║
// ║  K_fire(V=0.85) ≈ 1.07, K_fire(V=0) ≈ 1.57            ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_kfire_range() {
    let k_intact = k_fire(0.85);
    let k_bare = k_fire(0.0);

    println!(
        "K_fire range: V=0.85 → {:.4}, V=0.0 → {:.4}",
        k_intact, k_bare
    );

    // При интактном лесе: незначительная поправка
    assert!(
        (k_intact - 1.07).abs() < 0.05,
        "K_fire(V=0.85) = {:.4}, ожидается ~1.07",
        k_intact
    );

    // При полном уничтожении: +57% (согласно Fedorov 2022: +40–60%)
    assert!(
        k_bare >= 1.40 && k_bare <= 1.70,
        "K_fire(V=0) = {:.4}, ожидается ~1.57",
        k_bare
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 10 — Континентальность: Якутия vs умеренный       ║
// ║  f(ΔT=88°C) ≈ 1.10 — примерно 10% поправка            ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_continental_factor() {
    let f_moderate = f_continental(40.0); // базовый умеренный климат
    let f_yakutia = f_continental(88.0); // Центральная Якутия
    let f_extreme = f_continental(98.0); // Верхоянск (рекорд)

    println!("Continental factor:");
    println!("  Умеренный (ΔT=40°C): {:.4}", f_moderate);
    println!("  Якутск   (ΔT=88°C): {:.4}", f_yakutia);
    println!("  Верхоянск(ΔT=98°C): {:.4}", f_extreme);

    // При базовой амплитуде = 1.0 (нет поправки)
    assert!(
        (f_moderate - 1.0).abs() < 1e-9,
        "f(ΔT₀) должна быть точно 1.0, получено {}",
        f_moderate
    );

    // Для Якутии: ~10% поправка
    assert!(
        f_yakutia >= 1.05 && f_yakutia <= 1.15,
        "f(ΔT=88°C) = {:.4}, ожидается 1.05–1.15",
        f_yakutia
    );

    // Монотонность: чем больше амплитуда, тем больше поправка
    assert!(
        f_moderate < f_yakutia && f_yakutia < f_extreme,
        "f(ΔT) должна монотонно расти"
    );
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 11 — Граничные условия: нулевые значения         ║
// ║  Формула не должна падать на нулях и экстремумах        ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_edge_cases() {
    // Минимальный DDT (поздняя осень, мало тепла)
    let xi_min_ddt = atlasov_thaw_depth(1.2, 100.0, 0.30, 0.75, 88.0);
    assert!(
        xi_min_ddt > 0.0 && xi_min_ddt.is_finite(),
        "Минимальный DDT: результат должен быть положительным конечным числом"
    );

    // Максимальный DDT (аномально тёплый сезон)
    let xi_max_ddt = atlasov_thaw_depth(1.2, 2000.0, 0.30, 0.75, 88.0);
    assert!(
        xi_max_ddt > 0.0 && xi_max_ddt.is_finite() && xi_max_ddt < 20.0,
        "Максимальный DDT: глубина {:.2} м должна быть конечной и < 20 м",
        xi_max_ddt
    );

    // I_ice = I0 (порог): K_ed = 1.0, просадка = базовая
    let s_threshold = atlasov_subsidence(1.0, I0, 0.1);
    assert!(
        (s_threshold - 0.1).abs() < 1e-9,
        "При I_ice = I0: просадка должна быть 1.0 * 1.0 * 0.1 = 0.1"
    );

    // I_ice < I0 (обычный грунт): K_ed = 1.0
    let s_normal = atlasov_subsidence(1.0, 0.20, 0.1);
    assert!(
        (s_normal - 0.1).abs() < 1e-9,
        "При I_ice < I0: просадка = ξ * K_comp"
    );

    println!("Edge cases: все граничные условия пройдены");
}

// ╔══════════════════════════════════════════════════════════╗
// ║  ТЕСТ 12 — Сводный: все три региона Якутии             ║
// ║  Северная (DDT~700) < Центральная (DDT~1000)           ║
// ║             < Южная (DDT~1300) по глубине протаивания   ║
// ╚══════════════════════════════════════════════════════════╝
#[test]
fn test_regional_comparison() {
    // Северная Якутия
    let xi_north = atlasov_thaw_depth(
        0.9,   // λₜ — торфяно-суглинистый промёрзший грунт
        700.0, // DDT — короткое лето
        0.40,  // w — высокая льдистость
        0.70,  // V — кустарниковая тундра
        95.0,  // ΔT — экстремальная амплитуда севера
    );

    // Центральная Якутия
    let xi_central = atlasov_thaw_depth(
        1.2,    // λₜ — суглинок
        1000.0, // DDT — среднее значение
        0.32,   // w
        0.80,   // V — лиственничная тайга
        88.0,   // ΔT
    );

    // Южная Якутия
    let xi_south = atlasov_thaw_depth(
        1.5,    // λₜ — суглинок менее льдистый
        1300.0, // DDT — более тёплое лето
        0.25,   // w — меньшая льдистость
        0.75,   // V
        75.0,   // ΔT — менее резкий климат
    );

    println!("Regional comparison:");
    println!("  Северная Якутия:    {:.3} м", xi_north);
    println!("  Центральная Якутия: {:.3} м", xi_central);
    println!("  Южная Якутия:       {:.3} м", xi_south);

    // Диапазон Brouchkov (2004): 1–4 м для Центральной Якутии
    assert!(
        xi_central >= 1.0 && xi_central <= 4.0,
        "ЦЯ: {:.3} м вне диапазона 1–4 м (Brouchkov 2004)",
        xi_central
    );

    // Юг должен протаивать глубже севера (больше тепла)
    assert!(
        xi_south > xi_north,
        "Юг ({:.3} м) должен протаивать глубже севера ({:.3} м)",
        xi_south,
        xi_north
    );

    println!("Все регионы в допустимых диапазонах ✓");
}
